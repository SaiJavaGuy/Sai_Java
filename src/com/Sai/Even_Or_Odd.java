package com.Sai;

public class Even_Or_Odd{
	
	public static void main(String []args) {
		
		for (int i = 1; i <20 ; i++) {
		//System.out.println(i);
		if (i%2==0) {
			System.out.println(i + " Is a Even Number!");
		
		}else {
			System.out.println(i+ " Is a Odd Number");
		}
	}
	
	
	}

}
