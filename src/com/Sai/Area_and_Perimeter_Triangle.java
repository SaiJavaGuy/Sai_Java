package com.Sai;

public class Area_and_Perimeter_Triangle {
	
	public static void main(String[] args) {
		
// Finding the Area of Triangle
		
		float h = 3;
		float b = 4;
		float a = (float) ((1.0/2)*b*h);
		
		System.out.println("The area of the triangle is : " + a + " Square Inches");
// Finding the perimeter of Triangle
		
		float a1 = 3;
		float b1 = 4;
		float c = 5;
		float p = a1 + b1 + c ;
		
		System.out.println("The Perimeter of the Triangle is : " + p + " Inches");
	}

		
}
