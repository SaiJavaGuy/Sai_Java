package conditional_Statements;

public class If_Statements {
	

public static void main(String []args) {
	
	int n = 24;
	int r = (n%2);
		// System.out.println(r);
	if(r == 0) {
		System.out.println(" The Number "+ n + " is Even!");
	
	}else {
		System.out.println("The Number "+ n + " is Odd!");
	}
}
}
